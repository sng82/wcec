                        <h2 class="mb-2 text-2xl xl:text-3xl ">Brief History of the Armourers Hall</h2>
                        <hr class="mb-2">
                        <p>
                            The Hall, situated on the corner of Coleman Street and London Wall, is on the original site of ‘The Dragon and Two Shoppers’. The Armourers and Braziers Company has occupied this same site since 1346; taking a lease on the property in 1428 and acquiring the freehold in the 16th Century.
                        </p>
                        <p>
                            The Hall was one of the very few that escaped destruction in the Great Fire of 1666. However, members of the Company who had workshops in the surrounding districts were rendered homeless but were permitted to carry on their trade in the Hall for three months provided that no hammer or forge was used.
                        </p>
                        <p>
                            The Hall was enlarged in 1795, but the Court decided in 1839 to rebuild it completely. Together with its furnishings this cost £10,533.00. The lantern or dome of the Livery Hall was added in 1872.
                        </p>
                        <p>
                            On 29 December 1940, during a major blitz of London, the surrounding area was devastated, but the Hall again survived. The Armourers and Braziers are much indebted to an unknown fireman, who seeing the curtains of the Court Room ablaze, broke into the Hall and extinguished the flames. Although his identity may never be known, his quick thinking undoubtedly saved the Hall.
                        </p>
                        <p>
                            The Livery Hall, with bold mottoes high under the roof lantern, has a rich display of 16th Century and 17th Century arms and armour. The three George II chandeliers have been in constant use since they were made in the mid-1700s and together with the Men-At-Arms candelabra provide a unique candle-lit setting for banquets.
                        </p>
                        <p>
                            The Drawing Room or ‘Gold Room’ is dominated by many magnificent paintings including those of Sir Henry Lee and his mistress, Anne Vavasour, which flank the Lee Armour. On the floor is a superb copy of the famous Ardebil Persian carpet at the Victoria and Albert Museum, made for the Shah Tahmasp for the mosque in Ardebil.
                        </p>
                        <p>
                            The elegant and beautiful proportioned Court Room has a fine Regency dining table with late 18th Century mahogany chairs and contains Dutch and Elizabethan paintings together with an impressive array of documents including the Grant of Arms in 1556. The clock above the door, surrounded by the Coat of Arms was made for the Company in 1767.
                        </p>
                        <p>
                            The Library is a quiet oasis at the heart of the Hall with an attractive George II mahogany bookcase and some interesting Dutch and Scottish paintings.
